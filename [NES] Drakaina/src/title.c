void copyTitleScreen() {
	PPU.vram.address = 0x20;
	PPU.vram.address = 0x00;

	for(i = 0; i < 30 * 32; i++) {
		PPU.vram.data = titleScreen[i];
	}

	// color
    PPU.vram.address = 0x23;
    PPU.vram.address = 0xC0;
    for(i = 0; i < 40; i++) {
        PPU.vram.data = 0x00;
    }

    for(i = 40; i < 48; i++) {
        PPU.vram.data = 0xAA;
    }

    for(i = 48; i < 64; i++) {
        PPU.vram.data = 0x55;
    }


}